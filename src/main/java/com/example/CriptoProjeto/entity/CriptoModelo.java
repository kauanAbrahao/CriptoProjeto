package com.example.CriptoProjeto.entity;

public class CriptoModelo {

    private String id;
    private String name;
    private Integer mrkCapt;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getMrkCapt() {
        return mrkCapt;
    }

    public void setMrkCapt(Integer mrkCapt) {
        this.mrkCapt = mrkCapt;
    }
}
